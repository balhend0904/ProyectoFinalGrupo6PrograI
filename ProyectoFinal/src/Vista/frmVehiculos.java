    private void cargarTabla(){
        Vehiculo v = new Vehiculo ();
        ArrayList<Vehiculo> listVehiculos= v.getDatosVehiculos();
        
        DefaultTableModel modelTabla = (DefaultTableModel)tblVehiculos.getModel();
        modelTabla.setRowCount(0);
        for (Vehiculo i: listVehiculos){
            modelTabla.addRow(new Object[]{
                i.getPlaca(),
                i.getMarca(),
                i.getModelo(),
                i.getAnio(),
                i.getColor(),
                i.getDuenio()
            });
        }
    }

